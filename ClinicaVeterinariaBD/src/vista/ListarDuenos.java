    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:
        ResultSet rs = null;
        try {
            rs = objDueno.listarDueno(objDueno);
            
            while (rs.next()) {
                Object [] row = {rs.getString("nombre"), rs.getString("apellido"),rs.getShort("edad"),rs.getString("sexo"),
                    rs.getString("nombreMascota"),rs.getString("sexoMascota")};
               modelo.addRow(row);
            }
        } catch (Exception e) {
            System.out.println("Excepcion capturada al listar: "+e);
        }
    }//GEN-LAST:event_btnListarActionPerformed

