    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        ResultSet rs = null;
        tableBuscar.setModel(buscarModelo);
        try{
            int selectedRowIndex = tableBuscar.getSelectedRow();
            int selectId = selectedRowIndex + 1;
            buscarModelo.removeRow(selectedRowIndex);
            objDueno.setBorrarDueno(selectId);
            System.out.println(selectId+"boton eliminar");
            rs = objDueno.eliminarDueno(objDueno);
        }catch(Exception e){
            JOptionPane.showConfirmDialog(null, e);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

